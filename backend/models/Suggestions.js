const mongoose = require("mongoose");
const suggestionSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    subject: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
const Suggestions = mongoose.model("Suggestions", suggestionSchema);
module.exports = Suggestions;
